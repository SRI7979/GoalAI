export function normalizeThemeId(themeId) {
  return APP_THEMES[themeId] ? themeId : 'default'
}

export function getDashboardThemeVars(themeId = 'default') {
  return APP_THEMES[normalizeThemeId(themeId)].dashboardVars
}

export function getPathWorlds(themeId = 'default') {
  const key = normalizeThemeId(themeId)
  const explicitWorlds = PATH_WORLD_COLORS[key]
  if (explicitWorlds) {
    return WORLD_META.map((world, index) => ({
      ...world,
      ...explicitWorlds[index],
    }))
  }

  const vars = APP_THEMES[key].dashboardVars
  const accents = [
    vars['--theme-primary'],
    vars['--theme-secondary'],
    vars['--theme-mastery'],
    vars['--theme-highlight'],
    vars['--theme-warm'],
  ]
  return WORLD_META.map((world, index) => ({
    ...world,
    accent: accents[index],
    dark: vars['--theme-bg'],
    glow: `${accents[index]}66`,
    bg: `linear-gradient(160deg, ${vars['--theme-primary-dim']} 0%, ${vars['--theme-surface']} 100%)`,
    strip: vars['--theme-primary-dim'],
    node: accents[index],
    lock: vars['--theme-border'],
  }))
}

export function getStoredOwnedThemes() {
  if (typeof window === 'undefined') return []
  try {
    const parsed = JSON.parse(localStorage.getItem(THEME_STORAGE_KEYS.owned) || '[]')
    if (!Array.isArray(parsed)) return []
    return Array.from(new Set(parsed.filter((themeId) => PURCHASABLE_THEME_IDS.includes(themeId))))
  } catch {
    return []
  }
}

export function setStoredOwnedThemes(themeIds) {
  if (typeof window === 'undefined') return
  const clean = Array.from(new Set((themeIds || []).filter((themeId) => PURCHASABLE_THEME_IDS.includes(themeId))))
  localStorage.setItem(THEME_STORAGE_KEYS.owned, JSON.stringify(clean))
}

export function unlockStoredTheme(themeId) {
  const cleanThemeId = normalizeThemeId(themeId)
  if (!PURCHASABLE_THEME_IDS.includes(cleanThemeId)) return getStoredOwnedThemes()
  const next = Array.from(new Set([...getStoredOwnedThemes(), cleanThemeId]))
  setStoredOwnedThemes(next)
  return next
}

export function getStoredActiveTheme(ownedThemes = null) {
  if (typeof window === 'undefined') return 'default'
  const storedTheme = normalizeThemeId(
    localStorage.getItem(THEME_STORAGE_KEYS.appearance) ||
    localStorage.getItem(THEME_STORAGE_KEYS.active) ||
    'default',
  )
  return APP_THEMES[storedTheme] ? storedTheme : 'default'
}

export function setStoredActiveTheme(themeId) {
  if (typeof window === 'undefined') return
  const nextThemeId = normalizeThemeId(themeId)
  localStorage.setItem(THEME_STORAGE_KEYS.active, nextThemeId)
  localStorage.setItem(THEME_STORAGE_KEYS.appearance, nextThemeId)
  window.dispatchEvent(new CustomEvent('pathai-theme-changed', { detail: { themeId: nextThemeId } }))
}

export function clearStoredActiveTheme() {
  if (typeof window === 'undefined') return
  localStorage.removeItem(THEME_STORAGE_KEYS.active)
  localStorage.removeItem(THEME_STORAGE_KEYS.appearance)
  window.dispatchEvent(new CustomEvent('pathai-theme-changed', { detail: { themeId: 'default' } }))
}
